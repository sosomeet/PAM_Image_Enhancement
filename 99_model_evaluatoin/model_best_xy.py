def gaussian_kernel(
    window_size: int,
    sigma: float,
    channels: int,
    dtype: torch.dtype,
    device: torch.device,
) -> torch.Tensor:
    coordinates = torch.arange(window_size, dtype=dtype, device=device)
    coordinates -= window_size // 2

    gaussian = torch.exp(-(coordinates**2) / (2.0 * sigma**2))
    gaussian /= gaussian.sum()

    kernel_2d = gaussian[:, None] @ gaussian[None, :]
    return kernel_2d.expand(channels, 1, window_size, window_size).contiguous()


def calculate_ssim_per_sample(
    prediction: torch.Tensor,
    target: torch.Tensor,
    data_range: float = PSNR_SSIM_DATA_RANGE,
    window_size: int = 11,
    sigma: float = 1.5,
) -> torch.Tensor:
    if prediction.shape != target.shape:
        raise ValueError(
            f"SSIM shape mismatch: {tuple(prediction.shape)} "
            f"vs {tuple(target.shape)}"
        )

    channels = prediction.shape[1]
    kernel = gaussian_kernel(
        window_size=window_size,
        sigma=sigma,
        channels=channels,
        dtype=prediction.dtype,
        device=prediction.device,
    )
    padding = window_size // 2

    mu_x = F.conv2d(prediction, kernel, padding=padding, groups=channels)
    mu_y = F.conv2d(target, kernel, padding=padding, groups=channels)

    mu_x_sq = mu_x.pow(2)
    mu_y_sq = mu_y.pow(2)
    mu_xy = mu_x * mu_y

    sigma_x_sq = (
        F.conv2d(prediction * prediction, kernel, padding=padding, groups=channels)
        - mu_x_sq
    )
    sigma_y_sq = (
        F.conv2d(target * target, kernel, padding=padding, groups=channels)
        - mu_y_sq
    )
    sigma_xy = (
        F.conv2d(prediction * target, kernel, padding=padding, groups=channels)
        - mu_xy
    )

    c1 = (0.01 * data_range) ** 2
    c2 = (0.03 * data_range) ** 2

    numerator = (2.0 * mu_xy + c1) * (2.0 * sigma_xy + c2)
    denominator = (
        (mu_x_sq + mu_y_sq + c1)
        * (sigma_x_sq + sigma_y_sq + c2)
    )

    ssim_map = numerator / torch.clamp(
        denominator,
        min=torch.finfo(prediction.dtype).eps,
    )
    return ssim_map.flatten(1).mean(dim=1)


def calculate_psnr_from_mse(
    mse: float,
    data_range: float = PSNR_SSIM_DATA_RANGE,
) -> float:
    if mse <= 0:
        return float("inf")
    return 10.0 * math.log10((data_range**2) / mse)


def calculate_map_metrics(
    prediction_xy_map: np.ndarray,
    high_xy_map: np.ndarray,
) -> dict[str, float]:
    difference = (
        prediction_xy_map.astype(np.float64)
        - high_xy_map.astype(np.float64)
    )

    l1 = float(np.mean(np.abs(difference)))
    mse = float(np.mean(np.square(difference)))
    psnr = calculate_psnr_from_mse(mse)

    prediction_tensor = torch.from_numpy(prediction_xy_map).unsqueeze(0).unsqueeze(0)
    target_tensor = torch.from_numpy(high_xy_map).unsqueeze(0).unsqueeze(0)

    with torch.inference_mode():
        ssim = float(
            calculate_ssim_per_sample(
                prediction_tensor.float(),
                target_tensor.float(),
            ).item()
        )

    return {
        "l1": l1,
        "mse": mse,
        "psnr": psnr,
        "ssim": ssim,
    }


def mean_and_std(values: Iterable[float]) -> tuple[float, float]:
    array = np.asarray(list(values), dtype=np.float64)
    if array.size == 0:
        raise ValueError("Cannot summarize an empty metric sequence.")

    mean = float(np.mean(array))
    std = float(np.std(array, ddof=1)) if array.size > 1 else 0.0
    return mean, std


def normalize_lower_is_better(values: np.ndarray) -> np.ndarray:
    values = np.asarray(values, dtype=np.float64)
    finite = np.isfinite(values)

    if not finite.all():
        finite_values = values[finite]
        if finite_values.size == 0:
            return np.zeros_like(values)
        replacement = float(np.max(finite_values))
        values = np.where(finite, values, replacement)

    minimum = float(np.min(values))
    maximum = float(np.max(values))
    span = maximum - minimum

    if span <= COMPOSITE_EPS:
        return np.zeros_like(values)

    return (values - minimum) / span


def normalize_higher_is_better(values: np.ndarray) -> np.ndarray:
    values = np.asarray(values, dtype=np.float64)
    finite = np.isfinite(values)

    if np.isposinf(values).any():
        finite_values = values[np.isfinite(values)]
        if finite_values.size == 0:
            return np.zeros_like(values)
        replacement = float(np.max(finite_values) + 1.0)
        values = np.where(np.isposinf(values), replacement, values)

    if not np.isfinite(values).all():
        finite_values = values[np.isfinite(values)]
        if finite_values.size == 0:
            return np.zeros_like(values)
        replacement = float(np.min(finite_values))
        values = np.where(np.isfinite(values), values, replacement)

    minimum = float(np.min(values))
    maximum = float(np.max(values))
    span = maximum - minimum

    if span <= COMPOSITE_EPS:
        return np.zeros_like(values)

    return (maximum - values) / span


def build_composite_ranking(
    rows_by_model: dict[str, list[dict[str, Any]]],
) -> tuple[list[dict[str, Any]], dict[str, dict[str, float]]]:
    """
    Return volume ranking and per-model/per-volume composite scores.

    Equal weights are used for normalized L1 loss, reversed SSIM, and
    reversed PSNR. The final common score is the mean across all five models.
    """
    volume_order = [row["volume"] for row in rows_by_model[MODEL_ORDER[0]]]

    for registry_key in MODEL_ORDER[1:]:
        current_order = [row["volume"] for row in rows_by_model[registry_key]]
        if current_order != volume_order:
            raise RuntimeError(
                "Models were not evaluated on the same ordered volumes.\n"
                f"Reference: {volume_order[:5]} ...\n"
                f"{registry_key}: {current_order[:5]} ..."
            )

    scores_by_model: dict[str, dict[str, float]] = {}
    normalized_detail: dict[str, dict[str, dict[str, float]]] = {}

    for registry_key in MODEL_ORDER:
        rows = rows_by_model[registry_key]
        l1_values = np.asarray([row["l1"] for row in rows], dtype=np.float64)
        ssim_values = np.asarray([row["ssim"] for row in rows], dtype=np.float64)
        psnr_values = np.asarray([row["psnr"] for row in rows], dtype=np.float64)

        normalized_l1 = normalize_lower_is_better(l1_values)
        normalized_ssim_loss = normalize_higher_is_better(ssim_values)
        normalized_psnr_loss = normalize_higher_is_better(psnr_values)

        composite = (
            normalized_l1
            + normalized_ssim_loss
            + normalized_psnr_loss
        ) / 3.0

        scores_by_model[registry_key] = {}
        normalized_detail[registry_key] = {}

        for index, volume in enumerate(volume_order):
            scores_by_model[registry_key][volume] = float(composite[index])
            normalized_detail[registry_key][volume] = {
                "normalized_l1_loss": float(normalized_l1[index]),
                "normalized_ssim_loss": float(normalized_ssim_loss[index]),
                "normalized_psnr_loss": float(normalized_psnr_loss[index]),
                "model_composite_loss": float(composite[index]),
            }

    ranking_rows: list[dict[str, Any]] = []
    for volume in volume_order:
        model_scores = [
            scores_by_model[registry_key][volume]
            for registry_key in MODEL_ORDER
        ]

        row: dict[str, Any] = {
            "volume": volume,
            "overall_composite_loss": float(np.mean(model_scores)),
        }

        for registry_key in MODEL_ORDER:
            row[f"{registry_key}_composite_loss"] = (
                scores_by_model[registry_key][volume]
            )

        ranking_rows.append(row)

    ranking_rows.sort(
        key=lambda row: (float(row["overall_composite_loss"]), row["volume"])
    )

    return ranking_rows, scores_by_model


