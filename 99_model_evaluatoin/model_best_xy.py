def extract_prediction(output: Any) -> torch.Tensor:
    if isinstance(output, torch.Tensor):
        prediction = output
    elif isinstance(output, (tuple, list)):
        prediction = next(
            (item for item in output if isinstance(item, torch.Tensor)),
            None,
        )
        if prediction is None:
            raise TypeError("Model output tuple/list contains no tensor.")
    elif isinstance(output, dict):
        prediction = None
        for key in (
            "prediction",
            "pred",
            "output",
            "out",
            "enhanced",
            "final",
            "sample",
        ):
            value = output.get(key)
            if isinstance(value, torch.Tensor):
                prediction = value
                break

        if prediction is None:
            prediction = next(
                (
                    value
                    for value in output.values()
                    if isinstance(value, torch.Tensor)
                ),
                None,
            )

        if prediction is None:
            raise TypeError("Model output dictionary contains no tensor.")
    else:
        raise TypeError(f"Unsupported model output type: {type(output)}")

    if prediction.ndim == 3:
        prediction = prediction.unsqueeze(1)

    if prediction.ndim != 4:
        raise ValueError(
            f"Expected prediction [B,C,Y,Z], got {tuple(prediction.shape)}"
        )

    if prediction.shape[1] != OUTPUT_CHANNELS:
        raise ValueError(
            f"Expected {OUTPUT_CHANNELS} output channel, "
            f"got {prediction.shape[1]}"
        )

    return prediction


def run_model_inference(
    model: nn.Module,
    inputs: torch.Tensor,
    inference_mode: str,
    generator: Optional[torch.Generator],
) -> Any:
    if inference_mode == "direct":
        return model(inputs)

    if inference_mode != "diffusion":
        raise ValueError(f"Unsupported inference mode: {inference_mode}")

    sample_method = getattr(model, "sample", None)
    if callable(sample_method):
        signature = inspect.signature(sample_method)
        kwargs: dict[str, Any] = {}

        if "condition" in signature.parameters:
            kwargs["condition"] = inputs
        elif "conditioning" in signature.parameters:
            kwargs["conditioning"] = inputs
        elif "cond" in signature.parameters:
            kwargs["cond"] = inputs

        if "generator" in signature.parameters:
            kwargs["generator"] = generator
        if "show_progress" in signature.parameters:
            kwargs["show_progress"] = False

        if kwargs and any(
            key in kwargs for key in ("condition", "conditioning", "cond")
        ):
            return sample_method(**kwargs)

        try:
            return sample_method(inputs, generator=generator, show_progress=False)
        except TypeError:
            try:
                return sample_method(inputs, generator=generator)
            except TypeError:
                return sample_method(inputs)

    # Evaluator-compatible DDPM classes may implement model(condition).
    return model(inputs)


def infer_prediction_xy_map(
    model: nn.Module,
    pair: dict[str, Any],
    registry_key: str,
    device: torch.device,
    config: EvaluationConfig,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    dataset = PAMAdjacentYZTestDataset(
        low_path=pair["low_path"],
        high_path=pair["high_path"],
    )

    inference_mode = MODEL_REGISTRY[registry_key]["inference_mode"]
    batch_size = (
        config.diffusion_batch_size
        if inference_mode == "diffusion"
        else config.batch_size
    )

    loader_kwargs: dict[str, Any] = {
        "dataset": dataset,
        "batch_size": batch_size,
        "shuffle": False,
        "num_workers": config.num_workers,
        "pin_memory": device.type == "cuda",
        "drop_last": False,
    }
    if config.num_workers > 0:
        loader_kwargs["persistent_workers"] = True
        loader_kwargs["prefetch_factor"] = 2

    loader = DataLoader(**loader_kwargs)
    prediction_volume = np.empty(HIGH_VOLUME_SHAPE, dtype=np.float32)

    amp_enabled = config.use_amp and device.type == "cuda"
    generator: Optional[torch.Generator] = None

    if inference_mode == "diffusion":
        generator = make_device_generator(
            device=device,
            seed=stable_seed(
                config.diffusion_seed,
                registry_key,
                pair["volume_key"],
            ),
        )

    model.eval()
    progress = tqdm(
        loader,
        desc=f"    {pair['volume_key']}",
        leave=False,
        dynamic_ncols=True,
    )

    with torch.inference_mode():
        for batch in progress:
            inputs = batch["input"].to(
                device=device,
                dtype=torch.float32,
                non_blocking=True,
            )

            if config.use_channels_last and device.type == "cuda":
                inputs = inputs.contiguous(memory_format=torch.channels_last)

            with torch.autocast(
                device_type=device.type,
                dtype=torch.float16,
                enabled=amp_enabled,
            ):
                output = run_model_inference(
                    model=model,
                    inputs=inputs,
                    inference_mode=inference_mode,
                    generator=generator,
                )
                predictions = extract_prediction(output)

                if predictions.shape[-2:] != (Y_SIZE, Z_SIZE):
                    predictions = F.interpolate(
                        predictions,
                        size=(Y_SIZE, Z_SIZE),
                        mode="bilinear",
                        align_corners=False,
                    )

            batch_predictions = predictions[:, 0].float().cpu().numpy()
            x_indices = batch["x_index"]

            if isinstance(x_indices, torch.Tensor):
                x_indices = x_indices.tolist()

            for local_index, x_index in enumerate(x_indices):
                prediction_volume[int(x_index)] = batch_predictions[local_index]

    low_center_volume = dataset.get_low_center_volume()
    high_volume = dataset.get_high_volume()

    # No rotation, transpose, or flip is applied.
    low_xy_map = np.max(low_center_volume, axis=2).astype(np.float32)
    high_xy_map = np.max(high_volume, axis=2).astype(np.float32)
    prediction_xy_map = np.max(prediction_volume, axis=2).astype(np.float32)

    del prediction_volume
    del dataset

    return prediction_xy_map, low_xy_map, high_xy_map


