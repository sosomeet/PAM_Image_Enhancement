def discover_model_file(
    experiment_dir: Path,
    configured_name: Optional[str],
) -> Path:
    if configured_name is not None:
        exact_path = experiment_dir / configured_name
        if exact_path.exists():
            return exact_path

        target_normalized = re.sub(r"[^a-z0-9]", "", configured_name.lower())
        for candidate in experiment_dir.rglob("*.py"):
            normalized = re.sub(r"[^a-z0-9]", "", candidate.name.lower())
            if normalized == target_normalized:
                print(f"[INFO] File-name fallback matched: {candidate}")
                return candidate

        raise FileNotFoundError(f"Model Python file not found: {exact_path}")

    candidates = [
        path
        for path in experiment_dir.rglob("*.py")
        if path.name.lower() not in EXCLUDED_PYTHON_FILES
        and "__pycache__" not in path.parts
        and ".venv" not in path.parts
    ]
    candidates = sorted(candidates, key=natural_key)

    if not candidates:
        raise FileNotFoundError(
            f"No model Python file found under: {experiment_dir}"
        )

    if len(candidates) > 1:
        print(f"[WARNING] Multiple Python files under {experiment_dir.name}:")
        for candidate in candidates:
            print(f"          - {candidate.relative_to(experiment_dir)}")
        print(f"[WARNING] Using: {candidates[0].name}")

    return candidates[0]


def discover_final_checkpoint(experiment_dir: Path) -> Path:
    """Load only the explicit final model path; no checkpoint fallback."""
    candidates = [
        experiment_dir / "models_enhancement" / "final" / "model_final.pth",
        experiment_dir / "models_enhancement" / "final" / "model_final.pt",
    ]

    for candidate in candidates:
        if candidate.exists():
            return candidate

    raise FileNotFoundError(
        "Final checkpoint not found. Expected one of:\n"
        + "\n".join(str(path) for path in candidates)
    )


def import_module_from_path(module_path: Path, registry_key: str):
    resolved_path = module_path.resolve()
    module_name = f"pam_eval_{registry_key}_{abs(hash(str(resolved_path)))}"

    spec = importlib.util.spec_from_file_location(module_name, resolved_path)
    if spec is None or spec.loader is None:
        raise ImportError(f"Cannot create import spec for: {resolved_path}")

    module = importlib.util.module_from_spec(spec)

    # Required by decorators such as @dataclass during module execution.
    sys.modules[module_name] = module

    model_dir = str(resolved_path.parent)
    sys.path.insert(0, model_dir)

    try:
        spec.loader.exec_module(module)
    except Exception:
        sys.modules.pop(module_name, None)
        raise
    finally:
        try:
            sys.path.remove(model_dir)
        except ValueError:
            pass

    return module


def is_model_class(obj: Any) -> bool:
    return (
        inspect.isclass(obj)
        and issubclass(obj, nn.Module)
        and obj is not nn.Module
    )


def architecture_score(cls: type[nn.Module]) -> int:
    name = cls.__name__.lower()
    score = 0

    positive_tokens = {
        "unet": 250,
        "enhancer": 180,
        "generator": 260,
        "diffusion": 260,
        "ddpm": 280,
        "model": 80,
        "network": 100,
        "residual": 40,
        "wavelet": 30,
        "multistage": 30,
        "intensity": 30,
        "attention": 30,
        "vanilla": 40,
    }
    negative_tokens = {
        "block": 300,
        "layer": 280,
        "encoder": 180,
        "decoder": 180,
        "attentiongate": 350,
        "gate": 120,
        "discriminator": 600,
        "critic": 600,
        "loss": 350,
        "trainer": 350,
        "sampler": 250,
    }

    for token, weight in positive_tokens.items():
        if token in name:
            score += weight
    for token, penalty in negative_tokens.items():
        if token in name:
            score -= penalty

    return score


def select_model_class(
    module: Any,
    class_candidates: Iterable[str],
) -> type[nn.Module]:
    for class_name in class_candidates:
        obj = getattr(module, class_name, None)
        if is_model_class(obj):
            print(f"[INFO] Model class: {class_name}")
            return obj

    discovered = [
        obj
        for _, obj in inspect.getmembers(module, is_model_class)
        if obj.__module__ == module.__name__
    ]

    if not discovered:
        raise RuntimeError("No nn.Module class was found in the model file.")

    ranked = sorted(
        discovered,
        key=lambda cls: (architecture_score(cls), cls.__name__),
        reverse=True,
    )

    best = ranked[0]
    best_score = architecture_score(best)
    tied = [cls for cls in ranked if architecture_score(cls) == best_score]

    if best_score <= 0 or len(tied) > 1:
        raise RuntimeError(
            "Model class could not be selected unambiguously.\n"
            f"Discovered: {[cls.__name__ for cls in discovered]}\n"
            f"Top candidates: {[cls.__name__ for cls in tied]}"
        )

    print(f"[INFO] Auto-selected model class: {best.__name__}")
    return best


def safe_torch_load(path: Path) -> Any:
    try:
        return torch.load(path, map_location="cpu", weights_only=True)
    except TypeError:
        return torch.load(path, map_location="cpu")
    except Exception:
        print(
            "[WARNING] weights_only=True failed. Retrying with "
            "weights_only=False for this user-owned checkpoint."
        )
        try:
            return torch.load(path, map_location="cpu", weights_only=False)
        except TypeError:
            return torch.load(path, map_location="cpu")


def extract_state_dict(
    checkpoint: Any,
    registry_key: str,
) -> dict[str, torch.Tensor]:
    if isinstance(checkpoint, nn.Module):
        return checkpoint.state_dict()

    if not isinstance(checkpoint, dict):
        raise TypeError(f"Unsupported checkpoint type: {type(checkpoint)}")

    if registry_key == "gan":
        candidate_keys = (
            "generator_state_dict",
            "model_state_dict",
            "state_dict",
            "generator",
            "model",
        )
    else:
        candidate_keys = (
            "ema_model_state_dict",
            "ema_state_dict",
            "model_state_dict",
            "state_dict",
            "diffusion_state_dict",
            "denoiser_state_dict",
            "unet_state_dict",
            "model",
            "network",
            "net",
        )

    for key in candidate_keys:
        value = checkpoint.get(key)

        if isinstance(value, nn.Module):
            return value.state_dict()

        if isinstance(value, dict) and value:
            if all(isinstance(item, torch.Tensor) for item in value.values()):
                return value

    if checkpoint and all(
        isinstance(key, str) and isinstance(value, torch.Tensor)
        for key, value in checkpoint.items()
    ):
        return checkpoint

    raise KeyError(
        "No model state_dict found in checkpoint. "
        f"Available keys: {list(checkpoint.keys())[:40]}"
    )


def clean_state_dict_prefixes(
    state_dict: dict[str, torch.Tensor],
) -> dict[str, torch.Tensor]:
    prefixes = (
        "module.",
        "_orig_mod.",
        "model.",
        "network.",
        "net.",
    )

    cleaned = dict(state_dict)
    changed = True

    while changed:
        changed = False
        keys = list(cleaned.keys())
        for prefix in prefixes:
            if keys and all(key.startswith(prefix) for key in keys):
                cleaned = {
                    key[len(prefix):]: value
                    for key, value in cleaned.items()
                }
                changed = True
                break

    return cleaned


def _metadata_value(checkpoint: Any, key: str, default: Any) -> Any:
    if isinstance(checkpoint, dict) and key in checkpoint:
        return checkpoint[key]
    return default


