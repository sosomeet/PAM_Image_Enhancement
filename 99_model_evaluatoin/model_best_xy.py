def parse_arguments() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Evaluate final models from folders 05, 13, 14, 15, and 16 on "
            "identical unrotated X-Y MAP test volumes."
        )
    )

    parser.add_argument("--batch-size", type=int, default=8)
    parser.add_argument("--diffusion-batch-size", type=int, default=2)
    parser.add_argument("--num-workers", type=int, default=0)
    parser.add_argument(
        "--max-volumes",
        type=int,
        default=None,
        help="Evaluate only the first N common test volumes.",
    )
    parser.add_argument("--no-amp", action="store_true")
    parser.add_argument("--no-channels-last", action="store_true")
    parser.add_argument(
        "--non-strict",
        action="store_true",
        help="Load checkpoints with strict=False.",
    )
    parser.add_argument(
        "--device",
        choices=["auto", "cuda", "cpu"],
        default="auto",
    )
    parser.add_argument("--diffusion-seed", type=int, default=1234)
    parser.add_argument(
        "--colormap-upper-percentile",
        type=float,
        default=99.8,
        help="Shared HOT colormap upper percentile for the selected volume.",
    )
    parser.add_argument(
        "--no-individual-images",
        action="store_true",
        help="Save only the 2x4 comparison figure, not individual PNG files.",
    )

    return parser.parse_args()


def resolve_device(option: str) -> torch.device:
    if option == "cpu":
        return torch.device("cpu")

    if option == "cuda":
        if not torch.cuda.is_available():
            raise RuntimeError("CUDA was requested but is unavailable.")
        return torch.device("cuda")

    return torch.device("cuda" if torch.cuda.is_available() else "cpu")


def validate_arguments(args: argparse.Namespace) -> None:
    if args.batch_size <= 0:
        raise ValueError("--batch-size must be positive.")
    if args.diffusion_batch_size <= 0:
        raise ValueError("--diffusion-batch-size must be positive.")
    if args.num_workers < 0:
        raise ValueError("--num-workers cannot be negative.")
    if args.max_volumes is not None and args.max_volumes <= 0:
        raise ValueError("--max-volumes must be positive.")
    if not 0.0 < args.colormap_upper_percentile <= 100.0:
        raise ValueError(
            "--colormap-upper-percentile must be in the interval (0, 100]."
        )


