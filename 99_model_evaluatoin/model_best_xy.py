"""
PAM multi-model evaluation using the final checkpoints from folders
05, 13, 14, 15, and 16.

Evaluation policy
-----------------
1. Every model is evaluated on the same ordered LOW-HIGH test-volume pairs.
2. Only the following final checkpoints are loaded:

   <experiment>/models_enhancement/final/model_final.pth
   <experiment>/models_enhancement/final/model_final.pt

3. The input LOW data are already pre-generated 3-adjacent Y-Z samples:

   LOW  : [X, C, Y, Z] = [200, 3, 200, 512]
   HIGH : [X, Y, Z]    = [200, 200, 512]

4. Each model predicts all X-indexed Y-Z slices. The prediction volume is
   reconstructed as [X, Y, Z]. An unrotated X-Y MAP is then generated by:

       xy_map = np.max(volume, axis=2)

   No np.rot90(), transpose, flip, or other orientation transform is applied.

5. L1, PSNR, and SSIM are calculated from the unrotated X-Y MAP.
6. Final model logs report mean +/- standard deviation across test volumes.
7. One common test volume is selected using the lowest overall composite loss.
   For each model, the following three loss directions are min-max normalized
   across the common test volumes and equally weighted:

       normalized L1 loss
       normalized SSIM loss  = reversed normalized SSIM
       normalized PSNR loss  = reversed normalized PSNR

   The per-volume composite score is averaged across all five models. The
   common volume with the lowest averaged composite loss is selected.
8. The selected volume is saved as one 2 x 4 comparison image:

       LOW | U-Net | Attention U-Net | Residual Learning
       GAN | DDPM  | HIGH            | Color Map

   Every image panel uses the same HOT colormap range.
"""

