def evaluate_model(
    registry_key: str,
    test_pairs: list[dict[str, Any]],
    device: torch.device,
    config: EvaluationConfig,
) -> tuple[dict[str, Any], list[dict[str, Any]], dict[str, np.ndarray]]:
    entry = MODEL_REGISTRY[registry_key]

    (
        model,
        model_file,
        checkpoint_path,
        class_name,
        init_kwargs,
    ) = build_and_load_model(
        registry_key=registry_key,
        device=device,
        strict=config.strict_checkpoint,
        use_channels_last=config.use_channels_last,
    )

    model_result_dir = RESULT_ROOT / registry_key
    model_result_dir.mkdir(parents=True, exist_ok=True)

    print("\n" + "=" * 96)
    print(f"Model          : {entry['display_name']}")
    print(f"Registry key   : {registry_key}")
    print(f"Model class    : {class_name}")
    print(f"Model file     : {model_file}")
    print(f"Final model    : {checkpoint_path}")
    print(f"Init kwargs    : {init_kwargs}")
    print(f"Inference mode : {entry['inference_mode']}")
    print(f"Volumes        : {len(test_pairs)}")
    print(f"Device         : {device}")
    print("Metric plane   : unrotated X-Y MAP = max over Z")
    print("=" * 96)

    per_volume_rows: list[dict[str, Any]] = []
    prediction_maps: dict[str, np.ndarray] = {}

    for index, pair in enumerate(test_pairs, start=1):
        print(
            f"[{index:03d}/{len(test_pairs):03d}] "
            f"{pair['volume_key']}"
        )

        prediction_xy_map, _, high_xy_map = infer_prediction_xy_map(
            model=model,
            pair=pair,
            registry_key=registry_key,
            device=device,
            config=config,
        )

        metrics = calculate_map_metrics(prediction_xy_map, high_xy_map)
        prediction_maps[pair["volume_key"]] = prediction_xy_map

        row = {
            "model_key": registry_key,
            "model_name": entry["display_name"],
            "volume": pair["volume_key"],
            "low_file": str(pair["low_path"]),
            "high_file": str(pair["high_path"]),
            "metric_plane": "XY_MAP_UNROTATED",
            **metrics,
        }
        per_volume_rows.append(row)

        print(
            "    "
            f"L1={metrics['l1']:.8f} | "
            f"MSE={metrics['mse']:.8f} | "
            f"PSNR={metrics['psnr']:.4f} dB | "
            f"SSIM={metrics['ssim']:.6f}"
        )

    save_csv(
        per_volume_rows,
        model_result_dir / "per_volume_metrics.csv",
    )

    mean_l1, std_l1 = mean_and_std(row["l1"] for row in per_volume_rows)
    mean_mse, std_mse = mean_and_std(row["mse"] for row in per_volume_rows)
    mean_psnr, std_psnr = mean_and_std(row["psnr"] for row in per_volume_rows)
    mean_ssim, std_ssim = mean_and_std(row["ssim"] for row in per_volume_rows)

    summary = {
        "model_key": registry_key,
        "model_name": entry["display_name"],
        "model_class": class_name,
        "num_volumes": len(per_volume_rows),
        "error_range_definition": "sample standard deviation across test volumes",
        "mean_l1": mean_l1,
        "std_l1": std_l1,
        "mean_mse": mean_mse,
        "std_mse": std_mse,
        "mean_psnr": mean_psnr,
        "std_psnr": std_psnr,
        "mean_ssim": mean_ssim,
        "std_ssim": std_ssim,
        "model_file": str(model_file),
        "checkpoint": str(checkpoint_path),
        "init_kwargs": init_kwargs,
    }

    with (model_result_dir / "summary.json").open(
        "w",
        encoding="utf-8",
    ) as file:
        json.dump(json_safe(summary), file, indent=2, ensure_ascii=False)

    print("-" * 96)
    print("Mean +/- standard deviation")
    print(f"  L1   = {mean_l1:.8f} +/- {std_l1:.8f}")
    print(f"  SSIM = {mean_ssim:.6f} +/- {std_ssim:.6f}")
    print(f"  PSNR = {mean_psnr:.4f} +/- {std_psnr:.4f} dB")

    del model
    if device.type == "cuda":
        torch.cuda.empty_cache()

    return summary, per_volume_rows, prediction_maps


