def robust_limits(
    images: Iterable[np.ndarray],
    upper_percentile: float,
) -> tuple[float, float]:
    values = np.concatenate(
        [np.asarray(image, dtype=np.float64).ravel() for image in images]
    )

    finite_values = values[np.isfinite(values)]
    if finite_values.size == 0:
        return 0.0, 1.0

    v_min = float(np.percentile(finite_values, 0.0))
    v_max = float(np.percentile(finite_values, upper_percentile))

    if v_max <= v_min:
        v_max = v_min + 1e-8

    return v_min, v_max


